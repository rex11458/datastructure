/*
 *  6.1)最小生成树
    在一个无向连通图G中，如果去除它的全部顶点和一部分边构成一个子图G‘
    若边集E(G')中的边即将图G中的所有顶点连通又不形成回路，则称子图G‘是原图G的一棵生成树。
    连通图的一次遍历所经过的边的集合及途中所有顶点的集合就构成了该图的一棵生成树，对连通图的不同遍历，就得倒了不同的生成树。
    其中具有权最小的生成树称为图的最小生成树。
    生成最小生成树的算法有两个，普里姆算法和克鲁斯卡算法。
 */


